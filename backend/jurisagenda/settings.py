def _load_local_env() -> None:
    env_path = BASE_DIR / ".env"
    if not env_path.exists():
        return

    for raw_line in env_path.read_text(encoding="utf-8").splitlines():
        line = raw_line.strip()
        if not line or line.startswith("#") or "=" not in line:
            continue

        key, value = line.split("=", 1)
        key = key.strip().removeprefix("export ").strip()
        value = value.strip()
        if len(value) >= 2 and value[0] == value[-1] and value[0] in {"'", '"'}:
            value = value[1:-1]

        os.environ.setdefault(key, value)


_load_local_env()


def _clean_host(value: str) -> str:
    return value.strip().removeprefix("https://").removeprefix("http://").rstrip("/")


def _split_env_hosts(value: str) -> list[str]:
    return [_clean_host(item) for item in value.split(",") if _clean_host(item)]


def _split_env_origins(value: str) -> list[str]:
    return [item.strip().rstrip("/") for item in value.split(",") if item.strip()]


def _env_flag(name: str, default: bool = False) -> bool:
    value = os.getenv(name)
    if value is None:
        return default
    return value.strip().lower() in {"1", "true", "yes", "on"}


def _clean_database_url(value: str) -> str:
    parsed = urlsplit(value)
    query = [
        (key, query_value)
        for key, query_value in parse_qsl(parsed.query, keep_blank_values=True)
        if key != "supa"
    ]
    return urlunsplit(
        (
            parsed.scheme,
            parsed.netloc,
            parsed.path,
            urlencode(query),
            parsed.fragment,
        )
    )


def _database_url_from_env() -> str:
    database_url = os.getenv("DATABASE_URL") or os.getenv("POSTGRES_URL")

    if not database_url:
        raise ImproperlyConfigured(
            "Defina DATABASE_URL com a string de conexão PostgreSQL. "
            "SQLite não é mais suportado neste projeto."
        )

    database_scheme = urlsplit(database_url).scheme
    if database_scheme in {"http", "https"}:
        raise ImproperlyConfigured(
            "DATABASE_URL deve ser uma string de conexão de banco, "
            "como postgresql://usuario:senha@host:5432/banco. "
            "Não use uma URL https:// de aplicativo, painel ou API."
        )

    return _clean_database_url(database_url)


